export type LessonLevel = '初級' | '中級' | '上級' | 'マニア級'

export type RecommendSlots = {
  /** article:'genre' で絞るジャンルID（taxonomy の実在ID） */
  genreId?: number
  /** キーワード絞り込み（ブランド名/カテゴリ名） */
  keyword?: string
  /** 複数キーワードをマージ取得（「吸引」のような gender-ambiguous な単一語を避ける用）。最優先。 */
  keywords?: string[]
  /** 指名ブランド（keyword 未指定時のアンカー） */
  brand?: string
  /** 一人/パートナーの軸（sceneMap で緩く絞る） */
  sceneAxis?: SceneAxis
  /** 価格帯（レベル相当の目安） */
  priceBand?: { min?: number; max?: number }
  /** 取得点数（既定6） */
  hits?: number
}

export type Faq = { q: string; a: string }

export type Lesson = {
  slug: string
  level: LessonLevel
  scene: SceneAxis
  /** ページ h1 / metadata title の核 */
  title: string
  /** SEO用 metadata title（未指定なら title にサフィックス付与）。主クエリ前寄せ・疑問形でCTR最適化。 */
  metaTitle?: string
  /** 狙う検索意図（参考・metadata description に活用） */
  intentKeyword: string
  /** 導入文（1〜2文・入口は健康/ウェルネストーン） */
  lead: string
  /** 本文段落（一般事実・使い方/選び方/衛生/素材） */
  body: string[]
  faq: Faq[]
  /** 読了点の商品提案ルール（動的解決） */
  recommend: RecommendSlots
  /** 関連レッスン slug（内部リンク網） */
  related?: string[]
}

